    private void loadDoctorNames(){
        ArrayList<String> doctorNames=null;
        try {
            doctorNames=ctrlQuery.getAllDoctorNames();
        } catch (Exception ex) {
            Logger.getLogger(RemoveDoctorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
         for (int i = 0; i < doctorNames.size(); i++){
            cmbDoctor.addItem(doctorNames.get(i));
        }
       
   }

