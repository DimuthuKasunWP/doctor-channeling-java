    private void cmbDoctorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDoctorItemStateChanged
         String id=(String) cmbDoctor.getSelectedItem();
            DoctorDTO  doctor = null;
        try {
            doctor = ctrlDoctor.search(id);
            
        } catch (Exception ex) {
            Logger.getLogger(RemoveDoctorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
            txtDoctorId.setText(doctor.getDrID());
        
            
        
        
    }//GEN-LAST:event_cmbDoctorItemStateChanged

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
      String id=txtDoctorId.getText();
        try {
            boolean result=ctrlDoctor.delete(id);
            if (result){
                JOptionPane.showMessageDialog(this, "Doctor has been successfully Deleted...!");
            }else{
                JOptionPane.showMessageDialog(this, "Doctor has not been Deleted...!");
            }
        } catch (Exception ex) {
            Logger.getLogger(RemoveDoctorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


