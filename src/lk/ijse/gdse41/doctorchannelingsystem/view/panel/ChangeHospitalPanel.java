    private void loadHospitalID(){
        ArrayList<String> allHospitalId=null;
        
        try {
            allHospitalId=ctrlQuery.getAllHospitalId();
        } catch (Exception ex) {
            Logger.getLogger(ChangeHospitalPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         for (int i = 0; i < allHospitalId.size(); i++){
            cmbHospital.addItem(allHospitalId.get(i));
        }
       
   }


