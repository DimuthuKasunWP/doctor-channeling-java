    private void cmbHospitalItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbHospitalItemStateChanged
       String hID=(String) cmbHospital.getSelectedItem();
        try {
            HospitalDTO hospital=ctrlQuery.getHospitalDetails(hID);
            txtHospital.setText(hospital.gethName());
        } catch (Exception ex) {
            Logger.getLogger(ChangeHospitalPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbHospitalItemStateChanged

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String hID=(String) cmbHospital.getSelectedItem();
            HospitalDTO hh=new HospitalDTO(
                    hID,
                    txtHospital.getText());
            boolean result=ctrlHospital.update(hh);
            if(result){
                JOptionPane.showMessageDialog(this,"Update is OK...!");
            }else{
                JOptionPane.showMessageDialog(this,"Update is failed");
            }
        } catch (Exception ex) {
            Logger.getLogger(ChangeHospitalPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


