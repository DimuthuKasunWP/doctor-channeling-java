    private void cmbScheduleIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbScheduleIdItemStateChanged
        try {
            String scheduleID=(String) cmbScheduleId.getSelectedItem();
            SearchQueryDTO sea=ctrlQuery.getScheduleDetails(scheduleID);
            txtDoctorName.setText(sea.getDrName());
            txtDoctorID.setText(sea.getDrID());
            txtDayName.setText(sea.getDayName());
            txtTimeSlot.setText(sea.getTimeSlot());
            txtMaxPatientCount.setText(sea.getMaxPatientCount());
        } catch (Exception ex) {
            Logger.getLogger(ChangeSchedulePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_cmbScheduleIdItemStateChanged

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       String scheduleID=(String) cmbScheduleId.getSelectedItem();
        ScheduleDTO schedule=new ScheduleDTO(
                scheduleID,
                txtDoctorID.getText(),
                txtDayName.getText(),
                txtTimeSlot.getText(),
                Integer.parseInt(txtMaxPatientCount.getText()));
        try {
            boolean result=ctrlSchedule.update(schedule);
            if (result){
                JOptionPane.showMessageDialog(this, "Schedule has been successfully Updated...!");
            }else{
                JOptionPane.showMessageDialog(this, "Schedule has not been Updated...!");
            }
        } catch (Exception ex) {
            Logger.getLogger(ChangeSchedulePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed


