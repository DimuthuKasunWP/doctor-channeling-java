    private void loadScheduleID(){
        ArrayList<String> allScheduleIds=null;
        
        try {
            allScheduleIds=ctrlQuery.getScheduleId();
        } catch (Exception ex) {
            Logger.getLogger(ChangeSchedulePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
      
         for (int i = 0; i < allScheduleIds.size(); i++){
            cmbScheduleId.addItem(allScheduleIds.get(i));
        }
       
   }

