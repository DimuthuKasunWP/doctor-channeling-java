    private void loadSpecialityID(){
        ArrayList<String> allSpecialityId=null;
        
        try {
            allSpecialityId=ctrlQuery.getAllSpecialityId();
        } catch (Exception ex) {
            Logger.getLogger(RemoveSpecialityPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         for (int i = 0; i < allSpecialityId.size(); i++){
            cmbSpeciality.addItem(allSpecialityId.get(i));
        }
       
   }


