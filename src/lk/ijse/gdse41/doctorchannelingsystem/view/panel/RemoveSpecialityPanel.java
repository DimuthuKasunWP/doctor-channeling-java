    private void cmbSpecialityItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbSpecialityItemStateChanged
       String spID=(String) cmbSpeciality.getSelectedItem();
        try {
            SpecialityDTO speciality=ctrlQuery.getSpecialityDetails(spID);
            txtSpeciality.setText(speciality.getSpType());
        } catch (Exception ex) {
            Logger.getLogger(RemoveSpecialityPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbSpecialityItemStateChanged

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        try {
            String spID=(String) cmbSpeciality.getSelectedItem();
            
            boolean result=ctrlSpeciality.delete(spID);
            if(result){
                JOptionPane.showMessageDialog(this,"Delete is OK...!");
            }else{
                JOptionPane.showMessageDialog(this,"Delete is failed");
            }
        } catch (Exception ex) {
            Logger.getLogger(RemoveSpecialityPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


